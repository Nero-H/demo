package po;

import java.io.Serializable;

import utility.TimePeriod;

public class SalesReportInfoPO implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	TimePeriod period;
	GoodsPO good;
	ClientPO client;
	UserPO user;
	String storeHouse;
	public SalesReportInfoPO(TimePeriod period, GoodsPO good, ClientPO client,
			UserPO user, String storeHouse) {
		this.period = period;
		this.good = good;
		this.client = client;
		this.user = user;
		this.storeHouse = storeHouse;
	}
	public TimePeriod getPeriod() {
		return period;
	}
	public void setPeriod(TimePeriod period) {
		this.period = period;
	}
	public GoodsPO getGood() {
		return good;
	}
	public void setGood(GoodsPO good) {
		this.good = good;
	}
	public ClientPO getClient() {
		return client;
	}
	public void setClient(ClientPO client) {
		this.client = client;
	}
	public UserPO getUser() {
		return user;
	}
	public void setUser(UserPO user) {
		this.user = user;
	}
	public String getStoreHouse() {
		return storeHouse;
	}
	public void setStoreHouse(String storeHouse) {
		this.storeHouse = storeHouse;
	}
	
	
}
